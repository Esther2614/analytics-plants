WITH t1 AS
(
    SELECT 
        ITEM_NAME,
        MIN(VIEWED_AT) AS FIRST_VIEWED_AT,
        MAX(VIEWED_AT) AS LAST_VIEWED_AT,
        COUNT(DISTINCT ITEM_VIEW_ID) AS COUNT_ITEM_VIEW,
        COUNT(DISTINCT SESSION_ID) AS COUNT_SESSION,
        SUM(ADD_TO_CART_QUANTITY) AS TOT_ADD_TO_CART_QUANTITY,
        SUM(REMOVE_FROM_CART_QUANTITY) AS TOT_REMOVE_FROM_CART_QUANTITY
    FROM {{ref('views')}}
    GROUP BY ITEM_NAME
),
t2 AS 
(
    SELECT
        ITEM_NAME,
        PRICE_PER_UNIT,
        ROW_NUMBER() OVER (PARTITION BY ITEM_NAME ORDER BY VIEWED_AT DESC) AS rn
    FROM {{ref('views')}}
), 
t3 AS 
(
    SELECT
        ITEM_NAME,
        PRICE_PER_UNIT AS CURRENT_PRICE_PER_UNIT
    FROM t2
    WHERE rn=1
)
SELECT
    t1.ITEM_NAME,
    t3.CURRENT_PRICE_PER_UNIT,
    t1.FIRST_VIEWED_AT,
    t1.LAST_VIEWED_AT,
    t1.COUNT_ITEM_VIEW,
    t1.COUNT_SESSION,
    t1.TOT_ADD_TO_CART_QUANTITY,
    t1.TOT_REMOVE_FROM_CART_QUANTITY,
    t1.TOT_ADD_TO_CART_QUANTITY-t1.TOT_REMOVE_FROM_CART_QUANTITY AS NET_CART_RETENTION
FROM t1
LEFT JOIN t3
ON t1.ITEM_NAME = t3.ITEM_NAME






    
    